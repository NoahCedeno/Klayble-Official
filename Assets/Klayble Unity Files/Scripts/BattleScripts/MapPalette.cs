/// <summary>
/// MapPalette will define how the maps "look", as in what Materials and Models are used for
/// certain map settings. If you're fighting in a castle, you probably will find more castle-y
/// assets rather than ones you'd find in an underworld or field.
/// </summary>
public enum MapPalette
{
    TEST = 0,
    Field = 1,
    Castle = 2,
    Underworld = 3
}